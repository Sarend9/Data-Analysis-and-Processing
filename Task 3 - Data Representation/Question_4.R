# Вопрос 4: В ходе опроса получены следующие данные о возникновении 
#           у респондентов трудностей при пользовании устройством:

# Задаем вектор ответов
otv_vector <- c(
  "никогда", "никогда", "редко", "постоянно", "никогда",
  "редко", "никогда", "редко", "часто", "постоянно"
)

# Объем наблюдений
cat("Объем наблюдений:", length(otv_vector), "\n")

# Создаем таблицу и сортируем по названиям
otv_table <- table(otv_vector)[c("никогда", "редко", "часто", "постоянно")]
cat("Таблица частот ответов:\n")
print(otv_table)

# Выводим накопленную частоту
cat("Накопленная частота:\n")
cum_freq <- cumsum(otv_table)
print(cum_freq)

# Выводим накопленную относительную частоту
cat("Накопленная относительная частота:\n")
cum_rel_freq <- cumsum(otv_table / length(otv_vector))
print(cum_rel_freq)